package com.baiheng.objectanimatordemo;

/**
 * 定义一个点对象
 */
public class Point {
    private int radius;

    public Point(int radius) {
        this.radius = radius;
    }

    public int getRadius() {
        return radius;
    }

    public void setRadius(int radius) {
        this.radius = radius;
    }

}

